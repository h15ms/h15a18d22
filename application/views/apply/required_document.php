					<h5>Visa on arrival for Indians</h5>
					<p>Certain countries require a Visa to enter but these are often issued in the destination country. Unlike a visa free entry where the tourist will receive a landing permission or slip the Visa on arrival is an actual Visa that is issued in the destination country. The Visas that are issued on arrival have clearer specifications on duration of stay and the sort of visits that can be undertaken. Fr instance Indian citizens cannot seek employment on a visa on arrival. The countries that offer Visa on arrival to Indian citizens are as follows: Bolivia, Burundi, Cambodia, Cape Verde, Comoros, Guinea Bissau, Guyana, Jordan, Laos, Madagascar, Malawi, Maldives, Marshal Islands, Mauritania, Palau, Saint Lucia, Samoa, Seychelles, Somalia, Tanzania, Thailand, Timorleste, Togo, Tuvalu, Uganda.</p>



















