					<h3>Afghan Visa Applicants</h3>
					<br>
					<h5>Visa for Afghan Nationals</h5>
					<br>
					<h6>Eligibility Conditions and Supporting Documents for Different Visa Categories:</h6>
					<br>
					<h6>All visa applications should be accompanied by copy of personal particular page of the passport.</h6> 
					<p>Visa applicants are required to submit own bank statement proving financial ability to pay all costs of the trip. In case of minor applicant (aged 15 and below) or a dependent, if they do not have individual bank account then they can submit bank statement of their parents/ spouse/ children.</p>
					
					<h5>Tourist Visa</h5>
					<p>Tourist Visa is granted to a Foreigner who does not have a residence or occupation in India and whose sole objective of visiting India is recreation, sight-seeing, casual visit to meet friends and relatives. No other activity is permissible on a Tourist Visa. The Mission would be issuing six months validity multiple entry Tourist visa for eligible visa applicants including Afghan nationals. The Tourist visa will be issued with the conditions that “each stay should not exceed 45 days”. For Afghan nationals there should be a gap of 60 days between two consecutive visits to India under a Tourist Visa. The Tourist Visa is non-extendable and non-convertible.</p>
					 
					<p><strong>Documents required</strong><br>	Duly filled Visa form with 2colourphotographs with white background (Size 2” X 2”), original ID (Taskara) and its photocopy, confirmed return air ticket and original passport.</p>
					<p><strong>Processing period</strong><br>	4-5  working days</p>
					<p><strong>Fee:</strong><br>	Gratis</p>

					<h5>Business Visa</h5>
					<p>The Business Visa is granted to a foreign national who wants to visit India to establish an industrial/business venture or to explore possibilities to set up industrial/business venture, wants to purchase/sell industrial products in India.  The applicant should be a person of assured financial standing and has the expertise in the field of the intended business.  Business visa is not granted for petty trading.  For more information on Business visa click on the link <a href="http://mha.nic.in/pdfs/work_visa_faq.pdf">http://mha.nic.in/pdfs/work_visa_faq.pdf</a></p>

					<p>Business visa is issued from the country of origin, or from the country of domicile of the foreigner, provided the period of permanent residence of that applicant in that particular country is more than two years.</p>
					 
					<p><strong>Documents required</strong><br>  Duly filled Visa form with 2 colour photographs with white background (Size 2” X 2”), original ID (Taskara) and its photocopy, Local company letter and original Jawaz and its photocopy, Invitation letter from India and copy the Indian company registration, confirmed return air ticket and original passport.Photocopy of current US$ bank account and receipts of business transaction during last visit on Business Visa. The receipts should be inclusive of VAT and other local taxes paid.</p>
					<p><strong>Processing period</strong><br>	4-5 working days</p>
					<p><strong>Fee:</strong><br>	Gratis</p>

					<h5>Medical Visa</h5>
					<p><strong>Documents required</strong><br>	Duly completed Visa form with 2 photographs (2’x2’), original ID (Taskara) and its photocopy, original medical papers and its photocopy, confirmed return air ticket.Photocopy of medical papers pertaining to last visit on Medical Visa may also be submitted. For medical treatment/check up for a longer and regular period, a letter from Indian Doctor/hospital must be enclosed.</p>
					<p><strong>Processing period</strong><br>	4-5  working days</p>
					<p><strong>Fee:</strong><br>	Gratis</p>

					<h5>Transit Visa</h5>
					<p><strong>Documents required</strong><br>	Duly completed Visa form with 2colour photos in white background (Size 2” X 2”), original ID (Taskara) and its photocopy, valid Visa for destination after India and confirmed onward air tickets.</p>
					<p><strong>Processing period</strong><br>	4-5 working days</p>
					<p><strong>Fee:</strong><br>	Gratis</p>

					<h5>Conference Visa</h5>
					<p><strong>Documents required</strong><br>	Duly completed Visa form with 2 colour photos in white background (Size 2” X 2”), Letter of invitation from the organizer of the conference, nomination letter from Afghan organization, original ID (Taskara) and its photocopy, and confirmed return air tickets.Photocopy of clearance from MHA & MEA for the conference is mandatory.</p>
					<p><strong>Processing period</strong><br>	4-5 working days</p>
					<p><strong>Fee:</strong><br>	Gratis</p>

					<h5>Student Visa/ Research Visa</h5>
					<p><strong>Documents required</strong><br>	Duly completed Visa form with 2 photographs (2’x2’), original ID (Taskara) and its photocopy, photocopy of admission or bonafideletter from concerned recognized educational institution in India, attested educational transcripts, financial guarantee from parents/legal guardian of the applicant.Photocopy of passport and bank statements of financial guarantor. For renewal of Student Visa, photocopy of last FRRO registration document is mandatory. Research visa applicants are required to produce original letter of admission from the Indian institutions (emails/ photocopies not acceptable).</p>
					<p><strong>Processing period</strong><br>	4-5 working days</p>
					<p><strong>Fee:</strong><br>	Gratis</p>

					<h5>Entry Visa</h5>
					<p>Entry Visa is granted to person’s spouse, children and dependent family members of a foreign national coming to India on long term visas like Employment; Studentand Research etc.  It is also granted to Persons of Indian Origin, his/her spouse and dependent children subject to production of proof of the PIO status, marriage certificate (in respect of spouse) and birth certificate (in respect of children). Spouses of an OCI/PIO card holders and dependent children can also apply for Entry Visa subject to production of necessary proof.</p>

					<p><strong>Documents Required</strong><br>	Duly filled Visa form with 2 colour photographs with white background (Size 2” X 2”), original ID (Taskara) and its photocopy, confirmed return air ticket and original passport. For those married to Indian nationals, Original Indian Passport of Spouse along with a photocopy, marriage certificate and photocopy. For children of either parent being an Indian passport holder, children’s birth certificate, one of the parents’ Indian passport original and photocopy.</p>
					<p><strong>Processing period</strong><br>	4-5  working days</p>
					<p><strong>Fee:</strong><br>	Gratis</p>

					<h5>Employment Visa</h5>
					<p>The Employment visa is granted to foreigners desiring to come to India for the purpose of employment.  The applicant should be a skilled and qualified professional or person who is being engaged or appointed by a company, organization, industry or undertaking, etc. in India on contract or employment basis at a senior level, skilled position such as technical expert, senior executive, or in a managerial position. For more information on Employment visa click on the link  <a href="http://mha.nic.in/pdfs/work_visa_faq.pdf">http://mha.nic.in/pdfs/work_visa_faq.pdf</a> </p>

					<p>The Employment Visa must be issued from the country of origin, or from the country of domicile of the foreigner, provided the period of permanent residence of that applicant in that particular country is for more than two years.</p>
					
					<p><strong>Documents required</strong><br>	Duly filled Visa form with 2 colour photographs with white background (Size 2” X 2”), original ID (Taskara) and its photocopy, Employment contract for the company in which they will be working, Indian company registration, academic certificateand original passport.</p>
					<p><strong>Processing period</strong><br>	4-5  working days</p>
					<p><strong>Fee:</strong><br>	Gratis</p>
					

					<p><strong>Note:</strong>    All Afghan nationals are required to register within 14 days of their arrival in
					India with the FRROs at Delhi, Mumbai, Chennai, Amritsarand Kolkata. For detailed instructions please visit <a href="http://indianfrro.gov.in/frro">http://indianfrro.gov.in/frro</a></p>






					<h3>Visa Requirements for India</h3>
					   
					<p>When travelling abroad, one is often plagued with the hurdles of Visa. Visa policy varies from country to country and the requirements and eligibility criteria are also dependent on the type of Visa one is applying for. Take for example a US Visa requires that an Indian citizen fills out different forms for different types of Visas. The Visa policy of India changes with every country and is the product of not just internal regulations but also a product of foreign policy. These administrative entry restrictions are put in place according to the respective country an Indian citizen wishes to visit. These restrictions may require Indian citizens to either avail a Visa beforehand or acquire a Visa upon arrival.</p>

					<h5>Visa Policy of India</h5>
					<p>The policy of India changes according to the country the Indian citizen wishes to visit. As o 2015, Indian citizens wishing to travel abroad would require no visa or a visa upon arrival while visiting 51 other countries with the remainder countries requiring a visa beforehand in order to travel. The required Visa again changes with the destination country. Countries like USA and UK require different Visas, so do countries like Australia. Countries in Europe require a different Visa as well. There are 51 countries that Indian citizens do not require a Visa to visit or can avail one upon arrival. These Visas are described below</p>

					<h5>No Visa Required for Indians</h5>
					<p>This is when the destination country does not require an Indian citizen to carry a Visa to visit. These countries may or may provide a permit to visit upon arrival and again changes from country to country. While most of the countries that fall into this bracket do publish some sort of permit slip upon arrival or set a maximum duration of stay there are certain countries such as Nepal or Bhutan that allow Indian citizens to roam freely and even seek employment. The visa free countries for Indians include: Bhutan, Dominica, Ecuador, El Salvador, Fiji, Grenada, Haiti, Indonesia, Jamaica, Mauritius, Micronesia, Nepal, Saint Kitts and Nevis, Saint Vincent and the Grenadines, Senegal, Trinidad and Tobago and Vanuatu.</p>

					<h5>E-Visa</h5>
					<p>E-Visa stands for electronic via and is a Visa that is applied for online. The Visa is obtained electronically after the applicant fills in the forms and makes the payment either through credit or debit card. The officers at the gates of the entry will then verify the passport through their system but travellers are advised to carry a printed copy of the E-Visa with them. The purpose of these E-Visas range from medical visits to recreational visits and even casual business visits. The applicant will require their passport to have a validity of at least 6 months from the date of issue of the visa. The procedure to fill out the e-visa forms vary from country to country and even India has opened its gates and offers E-Visas to over 113 countries. The list of countries that require Indian citizens to avail an E-Visa in order to visit are as follows:</p>
					
